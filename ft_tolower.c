#include <stdio.h>

int ft_tolower(int c)
{
	if (c >= 65 && c <= 90)
	{
		c = c + 32;
		return (c);
	}
	return (c);
}

int main(void)
{	
	int c;

	c = 'd';
	printf("%c", ft_tolower(c));
	return (0);
}


