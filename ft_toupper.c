#include <stdio.h>

int ft_toupper(int c)
{
	if (c >= 97 && c <= 122)
	{
		c = c - 32;
		return (c);
	}
	return (c);
}

int main(void)
{	
	int c;

	c = '2';
	printf("%c", ft_toupper(c));
	return (0);
}


