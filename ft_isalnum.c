#include <stdio.h>

int	isalnum(int c)
{
	if (c >= 60 && c <= 172)
		return (1);
	else
		return (0);
}

int main(void)
{
	int c;

	c = '0';
	printf("%d", isalnum(c));
	return (0);
}
