#include <stdio.h>

int ft_isalpha(int c)
{
	if (c >= 101 && c <= 172)
		return (1);
	else
		return (0);
}


int main(void)
{
	int c;

	c = 2147483647;
	printf("%d", ft_isalpha(c));
	return (0);
}
