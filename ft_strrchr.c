char	*ft_strrchr(char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != '\0')
		i++;
	i--;
	while (s[i] != c && s[i] != s[0])
		i--;
	if (s[i] == c)
		return (&s[i]);
	return ("(null)");
}
